echo -e "${BLUE}в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—${NC}"
echo -e "${BLUE}в•‘          Pre-Push Validation - CI/CD Simulation              в•‘${NC}"
echo -e "${BLUE}в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ${NC}"
echo ""

FAILED=0

# Function to run a check
run_check() {
    local name=$1
    local command=$2

    echo ""
    echo -e "${BLUE}в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ${NC}"
    echo -e "${YELLOW}рџ”Ќ Running: $name${NC}"
    echo -e "${BLUE}в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ${NC}"

    if (eval "$command"); then
        echo -e "${GREEN}вњ… PASSED: $name${NC}"
        return 0
    else
        echo -e "${RED}вќЊ FAILED: $name${NC}"
        FAILED=1
        return 1
    fi
}

# 1. Backend Lint
run_check "Backend ESLint" "cd backend && npm run lint"

# 2. Frontend Lint
run_check "Frontend ESLint" "cd frontend && npm run lint"

# 3. Backend Tests
run_check "Backend Tests" "cd backend && npm test -- --passWithNoTests --runInBand --forceExit"

# 4. Frontend Tests
run_check "Frontend Tests" "cd frontend && npm test -- --run"

# 5. Backend Build
run_check "Backend Build" "cd backend && npm run build"

# 6. Frontend Build
run_check "Frontend Build" "cd frontend && npm run build"

# 6b. Embed Widget Build
run_check "Embed Widget Build" "cd frontend && npm run build:embed"

# 7. Validate GitHub Workflows
if [ -f "./scripts/validate-workflows.sh" ]; then
    run_check "GitHub Workflows Validation" "./scripts/validate-workflows.sh"
else
    echo -e "${YELLOW}вљ пёЏ  Skipping workflow validation (script not found)${NC}"
fi

# Summary
echo ""
echo -e "${BLUE}в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—${NC}"
echo -e "${BLUE}в•‘                     Validation Summary                        в•‘${NC}"
echo -e "${BLUE}в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ${NC}"
echo ""

if [ $FAILED -eq 1 ]; then
    echo -e "${RED}вќЊ Some checks failed. Please fix the errors before pushing to GitHub.${NC}"
    echo ""
    exit 1
else
    echo -e "${GREEN}вњ… All checks passed successfully!${NC}"
    echo ""
    echo -e "${GREEN}рџљЂ Safe to push to GitHub for production!${NC}"
    echo ""
    exit 0
